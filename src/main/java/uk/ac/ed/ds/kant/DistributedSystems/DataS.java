package uk.ac.ed.ds.kant.DistributedSystems;

public class DataS{
	
	double time;
	
	double prob;

	public DataS(double time, double prob) {
		this.time = time;
		this.prob = prob;
	}
	public double getTime() {
		return time;
	}

	public void setTime(double time) {
		this.time = time;
	}

	public double getProb() {
		return prob;
	}

	public void setProb(double prob) {
		this.prob = prob;
	}
	
	
}